void APP_RTC_CallBack(U32 status)
{
	if(status & RTC_INT_ALARM_1)
	{
		LED_Toggle(LED_1);
	}

	if(status & RTC_INT_ALARM_2)
	{
		LED_Toggle(LED_2);
	}

	if(status & RTC_INT_TIMER)
	{
		LED_Toggle(LED_3);
	}

	if(status & RTC_INT_CALENDAR)
	{
		LED_Toggle(LED_4);
	}

	if(status & RTC_INT_TAMPER_1_DETECT)
	{
	}

	if(status & RTC_INT_TAMPER_2_DETECT)
	{
	}

	if(status & RTC_INT_TAMPER_3_DETECT)
	{
	}

	if(status & RTC_INT_TIME_STAMP_EVENT)
	{
	}
}
